def sets_():
    print("\033[34mSets in Python\033[0m")
    print('''A set is an unordered collection of unique elements.
''')
    print('''
    ✅ Creating a Set:
    numbers = {1, 2, 3, 4, 5}
    print(numbers)
''')
    print('''
    ✅ Adding and Removing Elements:
    numbers.add(6)  # Adds an element
    numbers.remove(3)  # Removes an element
''')
    print('''
    ✅ Set Operations:
    A = {1, 2, 3, 4}
    B = {3, 4, 5, 6} 
    print(A | B)  # Union: {1, 2, 3, 4, 5, 6}
    print(A & B)  # Intersection: {3, 4}
    print(A - B)  # Difference: {1, 2}
''')


